def isValidChessBoard(board):
    if totalCount(board, 'bking') != 1:
        print('This board is invalid.')
        print('Reason: Black can only have 1 King on the board.')
    elif totalCount(board, 'wking') != 1:
        print('This board is invalid.')
        print('Reason: White can only have 1 King on the board.')
    elif validSpace(board) == False:
        print('This board is invalid.')
        print('Reason: Invalid board space found (1A-8H only).')
    elif wSideCount(board) > 16:
        print('This board is invalid.')
        print('Reason: Too many total pieces on White.')
    elif bSideCount(board) > 16:
        print('This board is invalid.')
        print('Reason: Too many total pieces on Black.')
    elif (totalCount(board, 'bpawn') > 8 or totalCount(board, 'wpawn') > 8):
        print('This board is invalid.')
        print('Reason: One side has too many pawns.')
    else:
        print('\n' + 'This board is valid.')
        print('Number of pieces on the board...')
        print('\n' + 'White:')
        print('- King     ' + str(totalCount(board, 'wking')))
