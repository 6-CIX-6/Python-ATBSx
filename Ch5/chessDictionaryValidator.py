def totalCount(board, piece):
    pCount = 0  # pCount = number of pieces
    for k, v in board.items():
        if piece == v:
            pCount += 1
    return pCount

def wSideCount(board):
    wCount = 0  # wCount = number of pieces on White side
    wCount = (totalCount(board, 'wking') + totalCount(board, 'wqueen') + totalCount(board, 'wbishop') +
    totalCount(board, 'wknight') + totalCount(board, 'wrook') + totalCount(board, 'wpawn'))
    return wCount

def bSideCount(board):
    bCount = 0  # bCount = number of pieces on Black side
    bCount = (totalCount(board, 'bking') + totalCount(board, 'bqueen') + totalCount(board, 'bbishop') +
    totalCount(board, 'bknight') + totalCount(board, 'brook') + totalCount(board, 'bpawn'))
    return bCount

